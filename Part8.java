package comp2402a1;

import java.io.BufferedReader;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.PrintWriter;
import java.util.ArrayDeque;
import java.util.ArrayList;
import java.util.Comparator;
import java.util.List;

public class Part8 {
	
	/**
	 * Your code goes here - see Part0 for an example
	 * @param r the reader to read from
	 * @param w the writer to write to
	 * @throws IOException
	 */


	public static void doIt(BufferedReader r, PrintWriter w) throws IOException {
		ArrayDeque<String> lastLines = new ArrayDeque<>(1000);

		// Read each line from the input
		String line;
		while ((line = r.readLine()) != null) {
			if (lastLines.size() == 1000) {
				lastLines.removeFirst();
			}
			lastLines.addLast(line);
		}

		// Convert the deque to a list and sort it
		List<String> lineList = new ArrayList<>(lastLines);
		lineList.sort(Comparator.comparingInt(String::length).thenComparing(String::compareTo));

		// Output the sorted lines
		for (String sortedLine : lineList) {
			w.println(sortedLine);
		}
	}

	/**
	 * The driver.  Open a BufferedReader and a PrintWriter, either from System.in
	 * and System.out or from filenames specified on the command line, then call doIt.
	 * @param args
	 */
	public static void main(String[] args) {
		try {
			BufferedReader r;
			PrintWriter w;
			if (args.length == 0) {
				r = new BufferedReader(new InputStreamReader(System.in));
				w = new PrintWriter(System.out);
			} else if (args.length == 1) {
				r = new BufferedReader(new FileReader(args[0]));
				w = new PrintWriter(System.out);				
			} else {
				r = new BufferedReader(new FileReader(args[0]));
				w = new PrintWriter(new FileWriter(args[1]));
			}
			long start = System.nanoTime();
			doIt(r, w);
			w.flush();
			long stop = System.nanoTime();
			System.out.println("Execution time: " + 10e-9 * (stop-start));
		} catch (IOException e) {
			System.err.println(e);
			System.exit(-1);
		}
	}
}
